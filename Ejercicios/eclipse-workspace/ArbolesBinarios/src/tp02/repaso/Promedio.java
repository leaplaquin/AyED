package tp02.repaso;

public class Promedio {
	private float suma;
	private int cant;
	
	Promedio(){
	}

	public float getSuma() {
		return suma;
	}

	public void setSuma(float suma) {
		this.suma = suma;
	}

	public int getCant() {
		return cant;
	}

	public void setCant(int cant) {
		this.cant = cant;
	}
	

}
